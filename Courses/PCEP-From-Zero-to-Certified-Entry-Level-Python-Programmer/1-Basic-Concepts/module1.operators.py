# -----------------------------------------------------------------------------
# operators
# -----------------------------------------------------------------------------
print(5 + 4)              # add
# >>>      9
print(5 - 4)              # sub
# >>>      1
print(5 * 4)              # multiply
# >>>     20
print(6 / 2)              # division
## >>>   3.0              # a division result is a float
print(7 / 2)
# >>>    3.5
print(5 // 4)             # integer division
# >>>       1             # return the nearest whole number, rounded down
print(5 % 4)              # modulus
# >>>      1              # return the remainder of the division
# -----------------------------------------------------------------------------

# -----------------------------------------------------------------------------
# power operators
# -----------------------------------------------------------------------------
print(2 ** 3)             # 2 at the power of 3
# >>>       8
# -----------------------------------------------------------------------------
